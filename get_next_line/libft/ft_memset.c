#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char	*bb;
	size_t	i;

	bb = b;
	i = 0;
	if (len != 0)
	{
		while (i < len)
		{
			bb[i] = c;
			i++;
		}
	}
	return (b);
}
