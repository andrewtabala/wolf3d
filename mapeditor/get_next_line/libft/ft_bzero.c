#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ss;

	i = 0;
	ss = s;
	if (n != 0)
	{
		while (i < n)
		{
			ss[i] = '\0';
			i++;
		}
		s = ss;
	}
}
